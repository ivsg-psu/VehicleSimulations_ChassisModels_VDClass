%% script_test_fcn_VD_computePredictiveSafetySpeedProfile
%
% This script tests the function
%
%       fcn_VD_computePredictiveSafetySpeedProfile
%
% The function evaluates a predicted vehicle path against one or multiple
% path boundaries. If the predicted path intersects a boundary, the first
% intersection along the path is interpreted as a future safety violation.
%
% The existing speed planner is then used to calculate the maximum safe
% longitudinal-speed profile that satisfies the available-friction
% constraint and reaches zero speed at the collision station.
%
% When the requested initial speed is too high for the available stopping
% distance, the function also predicts the actual emergency-braking speed
% profile and the residual speed at the collision station.
%
% The following cases are evaluated:
%
% 1. A predicted path with one boundary intersection.
%
% 2. A predicted path with no boundary intersections.
%
% 3. A predicted path with two boundary intersections.
%
% 4. A curved predicted path, showing that the collision station is based
%    on accumulated distance along the path rather than global
%    x-coordinate.
%
% 5. A sample path generated using the Path Class Library.
%
% 6. A friction-coefficient sweep, showing the effect of available friction
%    on braking distance.
%
% 7. Several independent obstacles represented as multiple boundary paths.
%
% 8. A mapped pavement-boundary dataset used as a preliminary boundary
%    evaluation.
%
% 9. An infeasible stopping case in which the obstacle is too close for the
%    vehicle to stop from the requested initial speed. The test compares
%    the safe-speed limit with the predicted actual emergency-braking
%    profile and verifies the residual impact speed against an analytical
%    reference.
%
% 10. A Gao-based oval trajectory used as an additional non-trivial
%     predicted-path test case.
%
% 11. A mapped Test Track scenario comparing clockwise and
%     counter-clockwise approaches to the same physical obstacle. The test
%     evaluates the effect of path curvature on braking distance and on
%     longitudinal and lateral use of the available friction capacity.
%
% The tests verify:
%
% - Detected intersection points and first collision station.
% - Terminal-speed constraints.
% - Collision-related braking-start station and braking distance.
% - Normal operation when no collision is present.
% - Operation with multiple independent boundaries.
% - Sensitivity of braking distance to friction coefficient.
% - Prediction of residual impact speed when stopping is infeasible.
% - Compliance with the combined-friction constraint.
% - Direction-dependent braking behavior on the mapped Test Track.
%
% REVISION HISTORY:
%
% 2026_07_09 by Jaime Rodriguez
% - Created test script.
% - Added one-intersection test.
% - Added no-intersection test.
% - Added two-intersection test.
% - Added geometric visualization of the predicted path and boundary.
%
% 2026_07_10 by Jaime Rodriguez
% - Added tests for curved predicted paths.
% - Added tests for different friction coefficients.
% - Added tests for several independent obstacles.
% - Added tests using mapped Test Track boundaries.
% - Added test for infeasible stopping distance.
% - Added test using a Gao-based oval predicted path.
%
% 2026_07_18 by Jaime Rodriguez
% - Added prediction of the actual emergency-braking speed profile.
% - Added analytical verification of residual impact speed in Test 9.
% - Updated road geometry to use predicted-path curvature.
% - Added clockwise and counter-clockwise comparison at the same physical
%   obstacle in Test 11.
% - Added longitudinal and lateral friction-use analysis.
% - Added combined-friction and physical cornering-speed checks.
%
% TO-DO:
%
