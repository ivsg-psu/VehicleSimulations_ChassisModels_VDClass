%% BASIC test 11 - CW versus CCW stopping at the same physical obstacle
%
% This test uses the complete mapped Test Track boundaries to generate an
% approximate center path.
%
% A physical obstacle is positioned automatically at a location where the
% curvature during the clockwise approach is greater than the curvature
% during the counter-clockwise approach.
%
% The same physical obstacle, friction coefficient, initial speed and
% planner settings are used in both directions.
%
% The test compares:
%
% - Braking distance.
% - Safe speed profile.
% - Longitudinal acceleration.
% - Lateral acceleration.
% - Friction allocation.
%
% The obstacle station is shifted to s_relative = 0 in the comparison
% figures.

% 1. Locate and load the Test Track boundary data

currentFilePath = which( ...
    'script_test_fcn_VD_computePredictiveSafetySpeedProfile.m');

[currentFolder, ~, ~] = ...
    fileparts(currentFilePath);

repoRoot = fullfile( ...
    currentFolder, ...
    '..', ...
    '..');

dataFolder = fullfile( ...
    repoRoot, ...
    'Data');

dataFile = fullfile( ...
    dataFolder, ...
    'testTrackBoundariesXY.mat');

fprintf('\nLoading Test Track boundaries from:\n%s\n', ...
    dataFile);

assert(isfile(dataFile), ...
    'The Test Track boundary data file could not be found.');

loadedData = load(dataFile);

assert(isfield(loadedData, 'outerBoundaryXY'), ...
    'The data file must contain outerBoundaryXY.');

assert(isfield(loadedData, 'innerBoundaryXY'), ...
    'The data file must contain innerBoundaryXY.');

outerBoundaryXY = ...
    loadedData.outerBoundaryXY;

innerBoundaryXY = ...
    loadedData.innerBoundaryXY;

assert(size(outerBoundaryXY,2) == 2, ...
    'outerBoundaryXY must be an N-by-2 matrix.');

assert(size(innerBoundaryXY,2) == 2, ...
    'innerBoundaryXY must be an N-by-2 matrix.');

% 2. Remove invalid rows

outerBoundaryXY = outerBoundaryXY( ...
    ~any(isnan(outerBoundaryXY),2), :);

innerBoundaryXY = innerBoundaryXY( ...
    ~any(isnan(innerBoundaryXY),2), :);

assert(size(outerBoundaryXY,1) >= 3, ...
    'The outer boundary must contain at least three valid points.');

assert(size(innerBoundaryXY,1) >= 3, ...
    'The inner boundary must contain at least three valid points.');

% 3. Remove repeated closing points temporarily

closureTolerance = 1e-6;

if norm( ...
        outerBoundaryXY(end,:) - ...
        outerBoundaryXY(1,:)) < closureTolerance

    outerBoundaryXY = ...
        outerBoundaryXY(1:end-1,:);

end

if norm( ...
        innerBoundaryXY(end,:) - ...
        innerBoundaryXY(1,:)) < closureTolerance

    innerBoundaryXY = ...
        innerBoundaryXY(1:end-1,:);

end

% 4. Ensure that the two boundaries follow the same direction

outerClosedForArea = [
    outerBoundaryXY
    outerBoundaryXY(1,:)
];

innerClosedForArea = [
    innerBoundaryXY
    innerBoundaryXY(1,:)
];

outerSignedArea = 0.5 * sum( ...
    outerClosedForArea(1:end-1,1) .* ...
    outerClosedForArea(2:end,2) - ...
    outerClosedForArea(2:end,1) .* ...
    outerClosedForArea(1:end-1,2));

innerSignedArea = 0.5 * sum( ...
    innerClosedForArea(1:end-1,1) .* ...
    innerClosedForArea(2:end,2) - ...
    innerClosedForArea(2:end,1) .* ...
    innerClosedForArea(1:end-1,2));

if sign(outerSignedArea) ~= sign(innerSignedArea)

    innerBoundaryXY = ...
        flipud(innerBoundaryXY);

end

% 5. Align the starting points of the boundaries

distanceToOuterStart = sqrt(sum( ...
    (innerBoundaryXY - outerBoundaryXY(1,:)).^2, ...
    2));

[~, closestInnerStartIndex] = ...
    min(distanceToOuterStart);

innerBoundaryXY = [
    innerBoundaryXY(closestInnerStartIndex:end,:)
    innerBoundaryXY(1:closestInnerStartIndex-1,:)
];

% 6. Close the original boundaries

outerBoundaryClosed = [
    outerBoundaryXY
    outerBoundaryXY(1,:)
];

innerBoundaryClosed = [
    innerBoundaryXY
    innerBoundaryXY(1,:)
];

% 7. Calculate normalized station along each boundary

outerSegmentVectors = diff( ...
    outerBoundaryClosed, ...
    1, ...
    1);

outerSegmentLengths = sqrt(sum( ...
    outerSegmentVectors.^2, ...
    2));

outerStation = [
    0
    cumsum(outerSegmentLengths)
];

assert(outerStation(end) > 0, ...
    'The outer boundary must have a positive total length.');

outerNormalizedStation = ...
    outerStation ./ outerStation(end);

innerSegmentVectors = diff( ...
    innerBoundaryClosed, ...
    1, ...
    1);

innerSegmentLengths = sqrt(sum( ...
    innerSegmentVectors.^2, ...
    2));

innerStation = [
    0
    cumsum(innerSegmentLengths)
];

assert(innerStation(end) > 0, ...
    'The inner boundary must have a positive total length.');

innerNormalizedStation = ...
    innerStation ./ innerStation(end);

% 8. Resample both boundaries
%
% A relatively fine center-path discretization is used so that path
% smoothing does not remove the relevant curve geometry.

numberOfCenterPathPoints = 1200;

commonNormalizedStation = linspace( ...
    0, ...
    1, ...
    numberOfCenterPathPoints + 1)';

outerBoundaryResampledClosed = [
    interp1( ...
        outerNormalizedStation, ...
        outerBoundaryClosed(:,1), ...
        commonNormalizedStation, ...
        'linear'), ...
    interp1( ...
        outerNormalizedStation, ...
        outerBoundaryClosed(:,2), ...
        commonNormalizedStation, ...
        'linear')
];

innerBoundaryResampledClosed = [
    interp1( ...
        innerNormalizedStation, ...
        innerBoundaryClosed(:,1), ...
        commonNormalizedStation, ...
        'linear'), ...
    interp1( ...
        innerNormalizedStation, ...
        innerBoundaryClosed(:,2), ...
        commonNormalizedStation, ...
        'linear')
];

% 9. Generate the center path

centerPathCandidateClosed = 0.5 .* ( ...
    outerBoundaryResampledClosed + ...
    innerBoundaryResampledClosed);

centerPathCandidate = ...
    centerPathCandidateClosed(1:end-1,:);

outerBoundaryResampled = ...
    outerBoundaryResampledClosed(1:end-1,:);

innerBoundaryResampled = ...
    innerBoundaryResampledClosed(1:end-1,:);

assert(size(centerPathCandidate,1) >= 5, ...
    'The generated center path must contain at least five points.');

% 10. Normalize the center-path direction
%
% Positive signed area corresponds to a counter-clockwise point ordering.
% The path is normalized so that predictedPathCCW is always CCW.

centerPathCandidateClosed = [
    centerPathCandidate
    centerPathCandidate(1,:)
];

centerPathSignedArea = 0.5 * sum( ...
    centerPathCandidateClosed(1:end-1,1) .* ...
    centerPathCandidateClosed(2:end,2) - ...
    centerPathCandidateClosed(2:end,1) .* ...
    centerPathCandidateClosed(1:end-1,2));

if centerPathSignedArea < 0

    centerPathCandidate = ...
        flipud(centerPathCandidate);

    outerBoundaryResampled = ...
        flipud(outerBoundaryResampled);

    innerBoundaryResampled = ...
        flipud(innerBoundaryResampled);

end

predictedPathCCW = ...
    centerPathCandidate;

predictedPathCW = ...
    flipud(predictedPathCCW);

centerPathClosed = [
    predictedPathCCW
    predictedPathCCW(1,:)
];

% 11. Define planner settings
%
% Use 15.0 m/s to reproduce the previous test.
% Use 18.0 m/s to make the friction-allocation effect more visible.

comparisonSpeed = 20.0;

muValue = 0.6;
ds = 0.1;

plannerSettings.U_posted   = comparisonSpeed;
plannerSettings.U_initial  = comparisonSpeed;
plannerSettings.U_terminal = 0.0;
plannerSettings.lambda     = 0.9;
plannerSettings.g          = 9.81;

availableFrictionAcceleration = ...
    plannerSettings.lambda * ...
    muValue * ...
    plannerSettings.g;

referenceBrakingDistance = ...
    comparisonSpeed^2 / ...
    (2 * availableFrictionAcceleration);

% 12. Calculate periodic center-path curvature

numberOfPathPoints = ...
    size(predictedPathCCW,1);

closedCenterPathForSegments = [
    predictedPathCCW
    predictedPathCCW(1,:)
];

centerSegmentVectors = diff( ...
    closedCenterPathForSegments, ...
    1, ...
    1);

centerSegmentLengths = sqrt(sum( ...
    centerSegmentVectors.^2, ...
    2));

centerPathLength = ...
    sum(centerSegmentLengths);

assert(centerPathLength > 0, ...
    'The generated center path must have positive length.');

centerPathStation = [
    0
    cumsum(centerSegmentLengths(1:end-1))
];

previousPointIndices = [
    numberOfPathPoints
    (1:numberOfPathPoints-1)'
];

nextPointIndices = [
    (2:numberOfPathPoints)'
    1
];

incomingVectors = ...
    predictedPathCCW - ...
    predictedPathCCW(previousPointIndices,:);

outgoingVectors = ...
    predictedPathCCW(nextPointIndices,:) - ...
    predictedPathCCW;

signedCrossProducts = ...
    incomingVectors(:,1) .* outgoingVectors(:,2) - ...
    incomingVectors(:,2) .* outgoingVectors(:,1);

directionDotProducts = sum( ...
    incomingVectors .* outgoingVectors, ...
    2);

signedTurningAngles = atan2( ...
    signedCrossProducts, ...
    directionDotProducts);

incomingLengths = sqrt(sum( ...
    incomingVectors.^2, ...
    2));

outgoingLengths = sqrt(sum( ...
    outgoingVectors.^2, ...
    2));

localArcLengths = ...
    0.5 * (incomingLengths + outgoingLengths);

localArcLengths = max( ...
    localArcLengths, ...
    1e-6);

rawAbsoluteCurvature = ...
    abs(signedTurningAngles) ./ ...
    localArcLengths;

% 13. Smooth the periodic curvature signal
%
% The smoothing window is defined using a physical distance rather than a
% fixed number of path samples.

meanCenterPointSpacing = ...
    centerPathLength / numberOfPathPoints;

curvatureSmoothingDistance = 8.0;

curvatureSmoothingPoints = max( ...
    5, ...
    round( ...
        curvatureSmoothingDistance / ...
        meanCenterPointSpacing));

if mod(curvatureSmoothingPoints,2) == 0

    curvatureSmoothingPoints = ...
        curvatureSmoothingPoints + 1;

end

extendedCurvature = [
    rawAbsoluteCurvature
    rawAbsoluteCurvature
    rawAbsoluteCurvature
];

extendedCurvature = smoothdata( ...
    extendedCurvature, ...
    'movmedian', ...
    curvatureSmoothingPoints);

extendedCurvature = smoothdata( ...
    extendedCurvature, ...
    'movmean', ...
    curvatureSmoothingPoints);

absoluteCenterPathCurvature = ...
    extendedCurvature( ...
        numberOfPathPoints+1: ...
        2*numberOfPathPoints);

% 14. Calculate point-integration weights

previousSegmentLengths = [
    centerSegmentLengths(end)
    centerSegmentLengths(1:end-1)
];

pointIntegrationWeights = ...
    0.5 * ( ...
        previousSegmentLengths + ...
        centerSegmentLengths);

% 15. Select an obstacle position
%
% For a candidate obstacle:
%
% - The CCW approach consists of points behind the candidate in the current
%   CCW path ordering.
%
% - The CW approach consists of points ahead of the candidate in the
%   current CCW ordering.
%
% Weighted RMS curvature is used because lateral demand is directly related
% to curvature and enters quadratically in the friction-circle constraint.

rmsApproachCurvatureCCW = ...
    NaN(numberOfPathPoints,1);

rmsApproachCurvatureCW = ...
    NaN(numberOfPathPoints,1);

meanApproachCurvatureCCW = ...
    NaN(numberOfPathPoints,1);

meanApproachCurvatureCW = ...
    NaN(numberOfPathPoints,1);

for iCandidate = 1:numberOfPathPoints

    distanceBehindCandidate = mod( ...
        centerPathStation(iCandidate) - ...
        centerPathStation, ...
        centerPathLength);

    distanceAheadOfCandidate = mod( ...
        centerPathStation - ...
        centerPathStation(iCandidate), ...
        centerPathLength);

    ccwApproachMask = ...
        distanceBehindCandidate > 0 & ...
        distanceBehindCandidate <= ...
        referenceBrakingDistance;

    cwApproachMask = ...
        distanceAheadOfCandidate > 0 & ...
        distanceAheadOfCandidate <= ...
        referenceBrakingDistance;

    if any(ccwApproachMask)

        ccwWeights = ...
            pointIntegrationWeights(ccwApproachMask);

        ccwCurvature = ...
            absoluteCenterPathCurvature(ccwApproachMask);

        meanApproachCurvatureCCW(iCandidate) = ...
            sum(ccwCurvature .* ccwWeights) / ...
            sum(ccwWeights);

        rmsApproachCurvatureCCW(iCandidate) = sqrt( ...
            sum(ccwCurvature.^2 .* ccwWeights) / ...
            sum(ccwWeights));

    end

    if any(cwApproachMask)

        cwWeights = ...
            pointIntegrationWeights(cwApproachMask);

        cwCurvature = ...
            absoluteCenterPathCurvature(cwApproachMask);

        meanApproachCurvatureCW(iCandidate) = ...
            sum(cwCurvature .* cwWeights) / ...
            sum(cwWeights);

        rmsApproachCurvatureCW(iCandidate) = sqrt( ...
