    'Color', centerPathColor, ...
    'LineWidth', 2, ...
    'DisplayName', 'Generated center path');

plot( ...
    obstaclePath(:,1), ...
    obstaclePath(:,2), ...
    '-', ...
    'Color', obstacleColor, ...
    'LineWidth', 4, ...
    'DisplayName', 'Fixed physical obstacle');

plot( ...
    intersectionPointsCCW(:,1), ...
    intersectionPointsCCW(:,2), ...
    'x', ...
    'Color', intersectionColor, ...
    'MarkerSize', 13, ...
    'LineWidth', 3, ...
